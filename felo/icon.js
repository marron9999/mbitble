function arrayICON(icon) {
	let h = "";
	for(let y=0; y<5; y++) {
		let n = 0;
		for(let x=0; x<5; x++) {
			n = n * 2 + icon[y][x];
		}
		n = n.toString(16);
		if(n.length <= 1) n = "0" + n;
		h += n;
	}
	return h;
}
function valueICON(i) {
	return arrayICON(_icon[i]);
}

async function clickICOX(i) {
	event.stopPropagation();
	event.preventDefault();
	let xy = LED;
	if(i < 10) {
		LED = [];
		for(let y=0; y<5; y++) {
			LED[y] = [0,0,0,0];
		}
	}
	for(let y=0; y<5; y++) {
		if(i < 10) {
			for(let x=0; x<5; x++) {
				if(i == 0) {
					LED[y][x] = xy[4 - y][x];
				} else if(i == 1) {
					LED[y][x] = xy[y][4 - x];
				} else if(i == 2) {
					LED[y][x] = xy[x][4 - y];
				} else if(i == 3) {
					LED[y][x] = xy[4 - x][y];
				}
			}
		} else if(i == 10) {
			LED[0][y] = 1;
		} else if(i == 11) {
			LED[y][4] = 1;
		} else if(i == 12) {
			LED[4][y] = 1;
		} else if(i == 13) {
			LED[y][0] = 1;
		}
	}
	for(let y=0; y<5; y++) {
		for(let x=0; x<5; x++) {
			E("" + y + x).style.background = (LED[y][x])? "black" : "white";
		}
	}
	fireICON();
}
async function clickICON(i) {
	event.stopPropagation();
	event.preventDefault();
	for(let y=0; y<5; y++) {
		for(let x=0; x<5; x++) {
			LED[y][x] = _icon[i][y][x];
			E("" + y + x).style.background = (LED[y][x])? "black" : "white";
		}
	}
	fireICON();
}
async function clickLED(n) {
	event.stopPropagation();
	event.preventDefault();
	let y = parseInt(n.id);
	let x = y % 10;
	y = parseInt(y / 10);
	LED[y][x] = (LED[y][x] + 1) % 2;
	E(n.id).style.background = (LED[y][x])? "black" : "white";
	fireICON();
}
